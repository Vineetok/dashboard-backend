import pool from '../../../config/db.js';
import bcrypt from 'bcrypt';

/* ===============================
   GET ALL CUSTOMERS
================================= */
export const getAllUsers = async (req, res) => {
  try {
    const {rows} = await pool.query (`
      SELECT 
        id,
        adv_id,
        name,
        email,
        role,
        date_joined,
        mobile
      FROM tbl_registeredusers
      WHERE role = 'CUSTOMER'
      ORDER BY date_joined DESC
    `);

    res.status (200).json ({
      success: true,
      data: rows,
    });
  } catch (err) {
    console.error ('Get All Users Error:', err);
    res.status (500).json ({
      success: false,
      message: 'Failed to fetch users',
    });
  }
};

/* ===============================
   ADD USER
================================= */

export const addUser = async (req, res) => {
  try {
    const {name, email, mobile, password} = req.body;

    // 🔹 Validation
    if (!name || !email || !mobile || !password) {
      return res.status (400).json ({
        success: false,
        message: 'All fields are required',
      });
    }

    // 🔹 Hash password
    const hashedPassword = await bcrypt.hash (password, 12);

    // 🔹 Insert user (ADV ID auto-generated by DB)
    const result = await pool.query (
      `INSERT INTO tbl_registeredusers
       (name, email, mobile, password, role)
       VALUES ($1, $2, $3, $4, $5)
       RETURNING id, adv_id, name, email, mobile, role`,
      [
        name.trim (),
        email.trim (),
        mobile.trim (),
        hashedPassword,
        'CUSTOMER', // Change role if needed
      ]
    );

    return res.status (201).json ({
      success: true,
      message: 'User created successfully',
      data: result.rows[0],
    });
  } catch (error) {
    console.error ('Add User Error:', error);

    // 🔹 Unique constraint error
    if (error.code === '23505') {
      return res.status (400).json ({
        success: false,
        message: 'Email or Mobile already exists',
      });
    }

    return res.status (500).json ({
      success: false,
      message: 'Failed to create user',
    });
  }
};

/* ===============================
   UPDATE USER (Admin)
================================= */
export const updateUser = async (req, res) => {
  try {
    const {id} = req.params;
    const {name, email, mobile} = req.body;

    const result = await pool.query (
      `UPDATE tbl_registeredusers
       SET name = COALESCE($1, name),
           email = COALESCE($2, email),
           mobile = COALESCE($3, mobile),
           updated_at = NOW()
       WHERE id = $4
       RETURNING id, adv_id, name, email, mobile`,
      [name || null, email || null, mobile || null, id]
    );

    if (result.rows.length === 0) {
      return res.status (404).json ({
        success: false,
        message: 'User not found',
      });
    }

    res.status (200).json ({
      success: true,
      message: 'User updated successfully',
      data: result.rows[0],
    });
  } catch (error) {
    console.error ('Update User Error:', error);
    res.status (500).json ({
      success: false,
      message: 'Failed to update user',
    });
  }
};

/* ===============================
   DELETE USER
================================= */
export const deleteUser = async (req, res) => {
  try {
    const {id} = req.params;

    const result = await pool.query (
      `DELETE FROM tbl_registeredusers WHERE id = $1`,
      [id]
    );

    if (result.rowCount === 0) {
      return res.status (404).json ({
        success: false,
        message: 'User not found',
      });
    }

    res.status (200).json ({
      success: true,
      message: 'User deleted successfully',
    });
  } catch (error) {
    console.error ('Delete User Error:', error);
    res.status (500).json ({
      success: false,
      message: 'Failed to delete user',
    });
  }
};
